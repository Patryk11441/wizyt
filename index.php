						$('.navi').addClass('sticky');
						$('.navi').removeClass('hide');
						
					} else {
						$('.navi').removeClass('sticky');
						$('.navi').addClass('hide');
					}
				};
				
				sticky();
				
				$(window).scroll(function() {
					sticky();
				});
				
			});
			
